// Карта токен -> массив всех синонимов из его группы (включая сам токен)
const SYNONYM_MAP = new Map<string, string[]>();
for (const group of SYNONYM_GROUPS) {
  const normalized = group.map((w) => w.toLowerCase());
  for (const phrase of normalized) {
    // для однословных — добавляем как есть
    const words = phrase.split(/\s+/);
    const targets = [phrase, ...words.filter((w) => w.length >= 3)];
    for (const key of targets) {
      const existing = SYNONYM_MAP.get(key);
      if (existing) {
        for (const w of normalized) if (!existing.includes(w)) existing.push(w);
      } else {
        SYNONYM_MAP.set(key, [...normalized]);
      }
    }
  }
}

export function expandWithSynonyms(tokens: string[]): string[] {
  const out = new Set<string>(tokens);
  for (const t of tokens) {
    const group = SYNONYM_MAP.get(t);
    if (group) {
      for (const g of group) {
        // добавляем и фразу, и отдельные слова из фразы
        out.add(g);
        for (const word of g.split(/\s+/)) if (word.length >= 3) out.add(word);
      }
    }
  }
  return [...out];
}
